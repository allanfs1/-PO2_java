package model;

/**
 *
 * @author Allan
 */
public class Produtos {
    
    private int id_pessoas;
    private String nome;
    private int idade;
    private String dt_nacimento;
    private String sexo;
    private String rg;
    private String cpf;

    public int getId_pessoas() {
        return id_pessoas;
    }

    public void setId_pessoas(int id_pessoas) {
        this.id_pessoas = id_pessoas;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getDt_nacimento() {
        return dt_nacimento;
    }

    public void setDt_nacimento(String dt_nacimento) {
        this.dt_nacimento = dt_nacimento;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
   
    
    
}
